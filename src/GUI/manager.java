    private void readorder() throws JAXBException
    {
        try {
            
        JAXBContext jax = JAXBContext.newInstance(Orders.class);
        Unmarshaller u = jax.createUnmarshaller();
        o = (Orders) u.unmarshal(new File("order.xml"));
        List<Order> order=o.getOrder();
        DefaultTableModel table = (DefaultTableModel) jTable4.getModel();
        
        for (int i=0;i<order.size();i++)
        {
            String name=order.get(i).getName();
            String tablenum=order.get(i).getTablenumber();
            String pricewout=order.get(i).getPricewithouttax();
            String pricewt=order.get(i).getPricewithtax();
            String ar[]={name,tablenum,pricewout,pricewt};
            table.addRow(ar);
            money+=Double.parseDouble(pricewout);
            moneyt+=Double.parseDouble(pricewt); 
        }
        
        jTextField1.setText(String.valueOf(money));
        jTextField2.setText(String.valueOf(moneyt));
        }
        catch (Exception e) {
            System.out.println(""+e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "No Orders Yet ");
        }
    }
