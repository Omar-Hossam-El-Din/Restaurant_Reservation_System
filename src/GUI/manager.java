    private void loadusers()
    {
        List<User> user=r.getUsersObject().getUsersList();
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        for (int i=0;i<user.size();i++)
        {
            String name =user.get(i).getName();
            String role=user.get(i).getRole();
            String data[]={name,role};
            table.addRow(data);
        }
    }
    
    private void loadtables()
    {
        List<Table> table =r.getTablesOject().getTablesList();
        DefaultTableModel t = (DefaultTableModel) jTable2.getModel();
        for (int i=0;i<table.size();i++)
        {
            String number =String.valueOf(table.get(i).getNumber());
            String seatnumber=String.valueOf(table.get(i).getNumber_of_seats());
            String smoking=String.valueOf(table.get(i).isSmoking());
            String data[]={number,seatnumber,smoking};
            t.addRow(data);
        }
        
    }
    private void loadmeals()
    {
        List<Dish> dish=r.getDishesObject().getDishesList();
        DefaultTableModel d = (DefaultTableModel) jTable3.getModel();
        for (int i=0;i<dish.size();i++)
        {
            String name =dish.get(i).getName();
            String price=String.valueOf(dish.get(i).getPrice());
            String type=dish.get(i).getType();
            String data[]={name,price,type};
            d.addRow(data);
        }
    }

