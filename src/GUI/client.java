    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int s=(int) jSpinner1.getValue();
        int index=jComboBox1.getSelectedIndex();
        boolean smoke=false;
        switch (index)
        {
            case 1:
            {
                smoke=true;
                break;
            }
            
            case 2:
            {
                smoke=false;
                break;
            }
            default :
            {
                JOptionPane.showMessageDialog(rootPane, "Select Smoking Availibility");
                break;
            }
        }
        List<Table> table=r.getTablesOject().getTablesList();
        Table availabletable=null;
        for(int i=0;i<table.size();i++)
        {
            if (table.get(i).getNumber_of_seats()>=s && table.get(i).isSmoking()==smoke)
            {
                availabletable=table.get(i);
                break;
            }
        }
        String avtable=String.valueOf(availabletable.getNumber());
        jTextField2.setText(avtable);
        
    }//GEN-LAST:event_jButton1ActionPerformed

