    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String index=jComboBox2.getSelectedItem().toString();
       DefaultTableModel d = (DefaultTableModel) jTable2.getModel();
       List<Dish> x=r.getDishesObject().getDishesList();
       Dish selecteddish=null;
       for (int i=0;i<x.size();i++)
       {
           if (x.get(i).getName().equalsIgnoreCase(index))
           {
               selecteddish=x.get(i);
               price+=x.get(i).getPrice();
               break;
           }
       }
       if (selecteddish==null)
       {
           JOptionPane.showMessageDialog(rootPane, "Select The Dish");
       }
       else
       {
       String l=selecteddish.getName();
       String m=String.valueOf(selecteddish.getPrice());
       String n=selecteddish.getType();
       String a[]={l,m,n};
       d.addRow(a);
       if (selecteddish.getType().equalsIgnoreCase("appetizer"))
           pricet+=selecteddish.getPrice()*0.1;
       else if(selecteddish.getType().equalsIgnoreCase("main_course"))
           pricet+=selecteddish.getPrice()*0.15;
       else if (selecteddish.getType().equalsIgnoreCase("desert"))
           pricet+=selecteddish.getPrice()*0.2;
       
       String t=String.valueOf(price);
       String u=String.valueOf(price+pricet);
       jTextField3.setText(t);
       jTextField4.setText(u);
       
       }
    }//GEN-LAST:event_jButton3ActionPerformed

